#How do you sort an array of integers in ascending & descending order?


list_1 = [21,2334,65457,58,67962,342,4564,23423,567]

print("The given array : ", list_1)

list_1.sort()
print("Sorted array in ascending order : ",list_1)


list_1.sort(reverse= True)
print("Sorted array in descending order : ",list_1)
